export function sectionSortValue(id) {
  const value = Number(id);
  return Number.isFinite(value) ? value : 9999;
}

export function buildSections(rows) {
  return rows
    .map((row) => {
      const id = String(row.section);
      return {
        id,
        title: sectionTitles[id] || row.section_name || `Розділ ${id}`,
        count: Number(row.count) || 0,
      };
    })
    .sort((left, right) => sectionSortValue(left.id) - sectionSortValue(right.id));
}
